//Criar uma função que escreva se um número é positivo ou negativo (considere o valor zero como positivo

function positivoNegativo (n1){
    let resposta = n1>=0 ? `O número ${n1} é positivo!`: `O número ${n1} é negativo!`;
    return resposta
}
